//"declaration" means creating a variable without assigning a value to it.
var a;
// "assignment" means giving a value to a declared variable.
a = 10;
// "Reassignment" refers to changing the value of an already declared variable.
a = 20;
// "redeclaration" means declaring a variable with the same name again and its overiding the previous value.
var a = 30; 
// "declaration" + "assignment" = "initialization", while "initialization" means creating a variable and assigning a value to it at the same time.
var b = 5;
// "redeclaration" means declaring a variable with the same name again in the same scope,it allowed in JavaScript, but it can lead to confusion and bugs, so it's generally recommended to avoid it.
var b = 15;
//output printing
console.log(a, b);
